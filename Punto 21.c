#include <stdio.h>

int main()
{
    int DDMMAAAA, dd, mm, aaaa;
    printf("Bienvenidos al programa en el que dada una fecha formato DDMMAAAA se separa esta misma en dias, mes y años utilizando operaciones aritmeticas \n");
    printf("Ingrese la fecha en formato DDMMAAAA \n");
    scanf("%d", &DDMMAAAA);
    aaaa=DDMMAAAA%10000;
    dd=DDMMAAAA/1000000;
    mm=(DDMMAAAA/10000)%100;
    printf("dia=%d mes=%d año=%d", dd, mm, aaaa);
    return 0;
}
